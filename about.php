                    <h2>About</h2>

                    <p style="text-align: left;">
                        Enlighten is a project we have been working on in our Grade 10 computer science class.
                        We started off with a sample project where we could add and remove rows from an SQL database.
                        We edited the sample code until we could add "tweets" and search for hashtags and users.
                        We also created a beautiful website to go with it.
                        <br><br>
                        The website uses HTML5 and CSS for the frontend, and PHP and SQL for the backend.
                        Bootstrap, StackOverFlow, W3Schools, Github, and Google were all extremely helpful resources and we
                        could not have made this website without them.
                        <br><br>
                        The programming was all done in two IDEs, Atom and Adobe Dreamweaver CS6. Each developer used
                        the IDE of their choice. MAMP was used to manage the website and run the MySQL and Apache servers.
                        Enlighten was made with and optimized for Google Chrome.
                        <br><br>
                        Creating this website was an excellent and enjoyable learning experience for all of the developers.
                        We started with little to no knowledge of any of the tools or languages involved.
                        Now all of us have the necessary skills to create and manage our own websites.
                    </p>

