                    <h2>FAQ and Help</h2>

                    <p style="text-align: left;">
                        <strong>Why are there no tweets?</strong>
                        <br>
                        Because you just downloaded an empty version of the website! With the release, we provided
                        an SQL database import script with a sample database of tweets and users for you to use.
                        <br><br>
                        <strong>Can I search for a user and a hashtag at the same time?</strong>
                        <br>
                        Yes, you absolutely can! Just type each search term into the correct box and click the GO!
                        button on either one of them. Any new search will automatically clear the old search.
